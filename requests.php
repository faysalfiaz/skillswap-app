<?php
session_start();
if (!isset($_SESSION['user_id'])) { header("Location: index.php"); exit(); }

require_once('db.php');
$my_uid = $_SESSION['user_id'];

// Clear real-time alerts safely
$conn->query("UPDATE Notifications SET is_read=1 WHERE user_id='$my_uid'");

if (isset($_POST['action_resolution'])) {
    $req_id = $conn->real_escape_string($_POST['request_id']);
    $resolution = $conn->real_escape_string($_POST['status_value']);
    
    if ($conn->query("UPDATE Project_Requests SET status='$resolution' WHERE request_id='$req_id' AND receiver_id='$my_uid'")) {
        $target_req = $conn->query("SELECT sender_id, project_title FROM Project_Requests WHERE request_id='$req_id'")->fetch_assoc();
        $sender_target = $target_req['sender_id'];
        $notif_text = $conn->real_escape_string($_SESSION['fullname'] . " has " . strtolower($resolution) . " your proposal: " . $target_req['project_title']);
        $conn->query("INSERT INTO Notifications (user_id, message) VALUES ('$sender_target', '$notif_text')");
    }
}

$incoming_proposals = $conn->query("SELECT Project_Requests.*, Users.fullname, Users.headline, Users.whatsapp_num 
                                    FROM Project_Requests JOIN Users ON Project_Requests.sender_id = Users.user_id 
                                    WHERE receiver_id='$my_uid' ORDER BY Project_Requests.created_at DESC");
?>
